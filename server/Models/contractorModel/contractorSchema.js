const mongoose = require("mongoose");

const Schema = mongoose.Schema;

const contractorSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    licenseNumber: {
      type: String,
      required: true,
    },
    phoneNumber: {
      type: Number,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Contractor = mongoose.model("Contractor", contractorSchema);
module.exports = Contractor;
